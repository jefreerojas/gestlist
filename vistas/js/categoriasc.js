$(".tablasc").on("click", ".btnEditarCategoria", function(){

	var idCategoriac = $(this).attr("idCategoriac");

	var datos = new FormData();
	datos.append("idCategoriac", idCategoriac);

	$.ajax({
		url: "ajax/categoriasc.ajax.php",
		method: "POST",
      	data: datos,
      	cache: false,
     	contentType: false,
     	processData: false,
     	dataType:"json",
     	success: function(respuesta){

     		$("#editarCategoria").val(respuesta["categoriac"]);
     		$("#idCategoria").val(respuesta["id"]);

     	}

	})


})

